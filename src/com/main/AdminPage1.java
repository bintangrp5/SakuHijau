    private void btn_simpantabunganActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpantabunganActionPerformed
        try {
            Connection conn = com.koneksi.koneksi.konek();
            String idNasabah = txt_tabung_idnasabah.getText().trim();
            String kodeSampah = txt_tabung_kdsampah.getText().trim();
            String jenisSampah = txt_tabung_jnssampah.getText().trim();
            String deskripsi = txt_tabung_deskripsi.getText().trim();
            String jenisCombo = cmb_tabung_jenis.getSelectedItem().toString().toLowerCase();
            String jenisMenabung = jenisCombo.contains("jemput") ? "Jemput" : "Datang";

            double beratKg = Double.parseDouble(txt_tabung_brtsampah.getText().trim());
            double hargaPerKg = Double.parseDouble(txt_tabung_hrgsampah.getText().trim());
            double total = Double.parseDouble(txt_tabung_total.getText().trim());

            java.util.Date tanggalUtil = dateChooser_tabung.getDate();
            java.sql.Date tanggalSql = new java.sql.Date(tanggalUtil.getTime());

            // =================== JEMPUT ===================
            if (jenisMenabung.equals("Jemput")) {
                // 1. Cek stok
                String cekStokSql = "SELECT stok FROM sampah WHERE kode_sampah = ?";
                PreparedStatement psCek = conn.prepareStatement(cekStokSql);
                psCek.setString(1, kodeSampah);
                ResultSet rsCek = psCek.executeQuery();

                if (rsCek.next()) {
                    double stokSekarang = rsCek.getDouble("stok");
                    if (stokSekarang < beratKg) {
                        JOptionPane.showMessageDialog(null, "Stok tidak mencukupi untuk transaksi ini.");
                        rsCek.close();
                        psCek.close();
                        return;
                    }
                }
                rsCek.close();
                psCek.close();

                // 2. Cek saldo
                String cekSaldoSql = "SELECT saldo FROM nasabah WHERE id = ?";
                PreparedStatement psSaldo = conn.prepareStatement(cekSaldoSql);
                psSaldo.setString(1, idNasabah);
                ResultSet rsSaldo = psSaldo.executeQuery();

                if (rsSaldo.next()) {
                    double saldo = rsSaldo.getDouble("saldo");
                    if (saldo >= total) {
                        int opsi = JOptionPane.showOptionDialog(null,
                                "Saldo mencukupi. Gunakan saldo untuk pembayaran?",
                                "Konfirmasi Pembayaran",
                                JOptionPane.YES_NO_OPTION,
                                JOptionPane.QUESTION_MESSAGE,
                                null,
                                new Object[]{"Gunakan Saldo", "Uang Pribadi"},
                                "Gunakan Saldo");

                        if (opsi == 0) {
                            // Gunakan saldo: hanya insert ke detail_tabungan (penarikan)
                            String updateSaldo = "UPDATE nasabah SET saldo = saldo - ? WHERE id = ?";
                            PreparedStatement psUpdateSaldo = conn.prepareStatement(updateSaldo);
                            psUpdateSaldo.setDouble(1, total);
                            psUpdateSaldo.setString(2, idNasabah);
                            psUpdateSaldo.executeUpdate();
                            psUpdateSaldo.close();

                            String insertPenarikan = "INSERT INTO penarikan_tabungan "
                                    + "(id_nasabah, jumlah, keterangan, tanggal) "
                                    + "VALUES (?, ?, ?, ?)";
                            PreparedStatement psPenarikan = conn.prepareStatement(insertPenarikan);
                            psPenarikan.setString(1, idNasabah);
                            psPenarikan.setDouble(2, total); // jumlah penarikan
                            psPenarikan.setString(3, "Penarikan untuk transaksi jemput"); // bisa kamu sesuaikan
                            psPenarikan.setDate(4, tanggalSql);
                            psPenarikan.executeUpdate();
                            psPenarikan.close();

                            // Kurangi stok
                            String updateStokSql = "UPDATE sampah SET stok = stok - ? WHERE kode_sampah = ?";
                            PreparedStatement psStok = conn.prepareStatement(updateStokSql);
                            psStok.setDouble(1, beratKg);
                            psStok.setString(2, kodeSampah);
                            psStok.executeUpdate();
                            psStok.close();

                            JOptionPane.showMessageDialog(null, "Transaksi berhasil menggunakan saldo.");
                            resetForm();
                            return;
                        }

                        if (opsi == JOptionPane.CLOSED_OPTION) {
                            // Jika popup ditutup silang
                            return;
                        }
                        // else lanjut ke uang pribadi
                    } else {
                        JOptionPane.showMessageDialog(null, "Saldo tidak mencukupi untuk transaksi ini.");
                        rsSaldo.close();
                        psSaldo.close();
                        return;
                    }
                }
                rsSaldo.close();
                psSaldo.close();
            }

            // =================== DATANG atau JEMPUT (uang pribadi) ===================
            String sqlTabungan = "INSERT INTO tabungan (id_nasabah, kode_sampah, jenis_sampah, deskripsi, jenis_menabung, berat_kg, harga_per_kg, total, tanggal) "
                    + "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement psTabung = conn.prepareStatement(sqlTabungan);
            psTabung.setString(1, idNasabah);
            psTabung.setString(2, kodeSampah);
            psTabung.setString(3, jenisSampah);
            psTabung.setString(4, deskripsi);
            psTabung.setString(5, jenisMenabung);
            psTabung.setDouble(6, beratKg);
            psTabung.setDouble(7, hargaPerKg);
            psTabung.setDouble(8, total);
            psTabung.setDate(9, tanggalSql);
            psTabung.executeUpdate();
            psTabung.close();

            String sqlDetail = "INSERT INTO detail_tabungan "
                    + "(id_nasabah, kode_sampah, jenis_sampah, deskripsi, berat_kg, harga_per_kg, setoran, tanggal) "
                    + "VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement psDetail = conn.prepareStatement(sqlDetail);
            psDetail.setString(1, idNasabah);
            psDetail.setString(2, kodeSampah);
            psDetail.setString(3, jenisSampah);
            psDetail.setString(4, deskripsi);
            psDetail.setDouble(5, beratKg);
            psDetail.setDouble(6, hargaPerKg);
            psDetail.setDouble(7, total);       // setoran
            psDetail.setDate(8, tanggalSql);    // tanggal
            psDetail.executeUpdate();
            psDetail.close();


// === Update saldo otomatis ===
            String updateSaldoOtomatis = "UPDATE nasabah n " +
        "LEFT JOIN ( " +
        "    SELECT dt.id_nasabah, " +
        "           IFNULL(SUM(dt.setoran), 0) AS total_setoran, " +
        "           IFNULL(MAX(pj.total_penarikan), 0) AS total_penarikan " +
        "    FROM detail_tabungan dt " +
        "    LEFT JOIN ( " +
        "        SELECT id_nasabah, SUM(jumlah) AS total_penarikan " +
        "        FROM penarikan_tabungan GROUP BY id_nasabah " +
        "    ) pj ON dt.id_nasabah = pj.id_nasabah " +
        "    GROUP BY dt.id_nasabah " +
        ") saldo ON n.id = saldo.id_nasabah " +
        "SET n.saldo = saldo.total_setoran - saldo.total_penarikan";


            PreparedStatement psUpdateSaldoFinal = conn.prepareStatement(updateSaldoOtomatis);
            psUpdateSaldoFinal.executeUpdate();
            psUpdateSaldoFinal.close();

            String updateStokSql = jenisMenabung.equals("Jemput")
                    ? "UPDATE sampah SET stok = stok - ? WHERE kode_sampah = ?"
                    : "UPDATE sampah SET stok = stok + ? WHERE kode_sampah = ?";

            PreparedStatement psStok = conn.prepareStatement(updateStokSql);
            psStok.setDouble(1, beratKg);
            psStok.setString(2, kodeSampah);
            psStok.executeUpdate();
            psStok.close();

            JOptionPane.showMessageDialog(null, "Data tabungan berhasil disimpan.");
            resetForm();

        } catch (SQLException | NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Gagal menyimpan data\n" + e.getMessage());
        }


    }//GEN-LAST:event_btn_simpantabunganActionPerformed

