    private void searchSampah(String keyword) {
        String[] header = {"Kode", "Jenis", "Deskripsi", "Stok"};
        DefaultTableModel model = new DefaultTableModel(null, header) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        try (Connection conn = com.koneksi.koneksi.konek()) {
            String sql = """
            SELECT kode_sampah, jenis, deskripsi, stok
            FROM sampah
            WHERE kode_sampah LIKE ? OR jenis LIKE ? OR deskripsi LIKE ? OR CAST(stok AS CHAR) LIKE ?
        """;

            PreparedStatement pst = conn.prepareStatement(sql);
            String param = "%" + keyword + "%";
            for (int i = 1; i <= 8; i++) {
                pst.setString(i, param);
            }

            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                Object[] row = {
                    rs.getString("kode_sampah"),
                    rs.getString("jenis"),
                    rs.getString("deskripsi"),
                    rs.getInt("stok")
                };
                model.addRow(row);
            }

            tabel_sampah.setModel(model);

            rs.close();
            pst.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal mencari data: " + e.getMessage());
            e.printStackTrace();
        }
    }

