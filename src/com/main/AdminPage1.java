    private void txt_jmlpenarikanKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_jmlpenarikanKeyReleased
        try {
            String idNasabah = txt_penarikan_idnasabah.getText().trim();

            if (idNasabah.isEmpty()) {
                lbl_penarikan_saldo.setText("Rp 0,00");
                lbl_penarikan_tarik.setText("Rp 0,00");
                lbl_penarikan_sisasaldo.setText("Rp 0,00");
                return;
            }

            Connection conn = com.koneksi.koneksi.konek();
            String sql = "SELECT saldo FROM nasabah WHERE id = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, idNasabah);
            ResultSet rs = ps.executeQuery();

            double saldo = 0;
            if (rs.next()) {
                saldo = rs.getDouble("saldo");
            }

            rs.close();
            ps.close();

            String strTarik = txt_jmlpenarikan.getText().trim();
            double penarikan = strTarik.isEmpty() ? 0 : Double.parseDouble(strTarik);

            double sisa = saldo - penarikan;

            NumberFormat rupiah = NumberFormat.getCurrencyInstance(new Locale("id", "ID"));

            lbl_penarikan_saldo.setText(rupiah.format(saldo));
            lbl_penarikan_tarik.setText(rupiah.format(penarikan));
            lbl_penarikan_sisasaldo.setText(rupiah.format(sisa));

        } catch (SQLException | NumberFormatException e) {
            lbl_penarikan_saldo.setText("Rp 0,00");
            lbl_penarikan_tarik.setText("Rp 0,00");
            lbl_penarikan_sisasaldo.setText("Rp 0,00");
        }
    }//GEN-LAST:event_txt_jmlpenarikanKeyReleased

