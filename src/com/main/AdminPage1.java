    private void btn_simpantabungan1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpantabungan1ActionPerformed
        String idNasabah = txt_penarikan_idnasabah.getText().trim();
        String jumlahTarikStr = txt_jmlpenarikan.getText().trim();

        if (idNasabah.isEmpty() || jumlahTarikStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Lengkapi data penarikan!");
            return;
        }

        try {
            double jumlahTarik = Double.parseDouble(jumlahTarikStr);

            Connection conn = com.koneksi.koneksi.konek();

            // 1. Ambil saldo lama nasabah
            String sqlSaldo = "SELECT saldo FROM nasabah WHERE id = ?";
            PreparedStatement psSaldo = conn.prepareStatement(sqlSaldo);
            psSaldo.setString(1, idNasabah);
            ResultSet rs = psSaldo.executeQuery();

            if (!rs.next()) {
                JOptionPane.showMessageDialog(this, "Nasabah tidak ditemukan.");
                return;
            }

            double saldoLama = rs.getDouble("saldo");
            if (jumlahTarik > saldoLama) {
                JOptionPane.showMessageDialog(this, "Saldo tidak cukup untuk penarikan.");
                return;
            }

            // 2. Simpan ke tabel penarikan_tabungan
            String sqlInsert = "INSERT INTO penarikan_tabungan (tanggal, id_nasabah, jumlah, keterangan) "
                    + "VALUES (?, ?, ?, ?)";
            PreparedStatement psInsert = conn.prepareStatement(sqlInsert);
            psInsert.setDate(1, new java.sql.Date(System.currentTimeMillis()));
            psInsert.setString(2, idNasabah);
            psInsert.setDouble(3, jumlahTarik);
            psInsert.setString(4, "Penarikan saldo oleh nasabah"); // keterangan opsional
            psInsert.executeUpdate();

            // 3. Update saldo nasabah
            String sqlUpdateSaldo = "UPDATE nasabah SET saldo = saldo - ? WHERE id = ?";
            PreparedStatement psUpdate = conn.prepareStatement(sqlUpdateSaldo);
            psUpdate.setDouble(1, jumlahTarik);
            psUpdate.setString(2, idNasabah);
            psUpdate.executeUpdate();

            // 4. Tampilkan sukses
            JOptionPane.showMessageDialog(this, "Penarikan berhasil disimpan.");

            // 5. Reset form
            txt_jmlpenarikan.setText("");
            txt_penarikan_idnasabah.setText("");
            txt_penarikan_nama.setText("");
            txt_penarikan_alamat.setText("");
            txt_penarikan_nohp.setText("");
            txt_penarikan_total.setText("");
            lbl_penarikan_saldo.setText("Rp 0,00");
            lbl_penarikan_tarik.setText("Rp 0,00");
            lbl_penarikan_sisasaldo.setText("Rp 0,00");

            loadTabelPenarikan();

        } catch (SQLException | NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data penarikan\n" + e.getMessage());
        }


    }//GEN-LAST:event_btn_simpantabungan1ActionPerformed

