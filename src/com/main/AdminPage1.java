            Connection conn = com.koneksi.koneksi.konek();
            String sql = "SELECT tanggal, jenis_sampah, deskripsi, harga_per_kg, berat_kg, "
                    + "CASE WHEN jenis_menabung = 'Datang' THEN total ELSE 0 END AS setoran, "
                    + "CASE WHEN jenis_menabung = 'Jemput' THEN total ELSE 0 END AS penarikan "
                    + "FROM tabungan WHERE id_nasabah = ? ORDER BY tanggal ASC";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, idNasabah);
