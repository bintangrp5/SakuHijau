    private void btn_simpahEditSampahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpahEditSampahActionPerformed
        String kode = txt_kodesampah_tambah.getText();
        String kategori = cmb_kategori_edtsampah.getSelectedItem().toString();
        String jenis = txt_jenissampah_edt.getText();
        String deskripsi = txt_deskripsi_edt.getText();
        String harga1Str = txt_hargadhs1_edt.getText();
        String harga2Str = txt_hargadhs2_edt.getText();
        String hargaPengepulStr = txt_hargapgpl_edt.getText();

        // Validasi dasar
        if (kode.isEmpty() || jenis.isEmpty() || deskripsi.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Isi semua data yang diperlukan!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        try {
            double harga1 = Double.parseDouble(harga1Str);
            double harga2 = Double.parseDouble(harga2Str);
            double hargaPengepul = Double.parseDouble(hargaPengepulStr);
            
            Connection conn = com.koneksi.koneksi.konek();
            String sql = "UPDATE sampah SET kategori = ?, jenis = ?, deskripsi = ?, harga1 = ?, harga2 = ?, harga_pengepul = ? WHERE kode_sampah = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, kategori);
            pst.setString(2, jenis);
            pst.setString(3, deskripsi);
            pst.setDouble(4, harga1);
            pst.setDouble(5, harga2);
            pst.setDouble(6, hargaPengepul);
            pst.setString(7, kode);
            
            int result = pst.executeUpdate();
            pst.close();
            conn.close();
            
            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data sampah berhasil diperbarui!");
                loadFormEditSampah(kode); 
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Data gagal diperbarui!", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Harga harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Kesalahan saat update: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_simpahEditSampahActionPerformed

