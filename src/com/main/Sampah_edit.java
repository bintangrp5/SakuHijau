    private void loadFormEditSampah(String kode) {
        txt_kodesampah_tambah.setText(kode); // langsung isi dari parameter

        try (Connection conn = com.koneksi.koneksi.konek()) {
            String sql = "SELECT kategori, jenis, deskripsi, harga1, harga2, harga_pengepul FROM sampah WHERE kode_sampah = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, kode);
            ResultSet rs = pst.executeQuery();
            
            if (rs.next()) {
                cmb_kategori_edtsampah.setSelectedItem(rs.getString("kategori"));
                txt_jenissampah_edt.setText(rs.getString("jenis"));
                txt_deskripsi_edt.setText(rs.getString("deskripsi"));
                txt_hargadhs1_edt.setText(String.valueOf(rs.getDouble("harga1")));
                txt_hargadhs2_edt.setText(String.valueOf(rs.getDouble("harga2")));
                txt_hargapgpl_edt.setText(String.valueOf(rs.getDouble("harga_pengepul")));
            }
            
            rs.close();
            pst.close();
            
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data edit: " + e.getMessage());
        }
    }
    
