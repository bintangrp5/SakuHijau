    public class SampahUtil {

        public static String generateKodeSampah(String jenis) {
            String prefix = getPrefixByJenis(jenis); // otomatis ambil 3 huruf pertama
            String kodeBaru = "";
            int nomorBaru = 1;

            try (Connection conn = com.koneksi.koneksi.konek()) {
                String sql = "SELECT kode_sampah FROM sampah WHERE kode_sampah LIKE ? ORDER BY kode_sampah DESC LIMIT 1";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, prefix + "%");
                ResultSet rs = pst.executeQuery();

                if (rs.next()) {
                    String lastKode = rs.getString("kode_sampah");
                    String angka = lastKode.substring(3);  
                    nomorBaru = Integer.parseInt(angka) + 1;
                }

                rs.close();
                pst.close();

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Gagal generate kode: " + e.getMessage());
            }

            kodeBaru = String.format("%s%05d", prefix, nomorBaru); // contoh: KER00001
            return kodeBaru;
        }

        private static String getPrefixByJenis(String jenis) {
            jenis = jenis.trim().toUpperCase();

            if (jenis.length() >= 3) {
                return jenis.substring(0, 3);
            } else {
                return String.format("%-3s", jenis).replace(' ', 'X'); // isi X jika kurang dari 3 huruf
            }
        }
    }

