    private void btn_simpahDataSampahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpahDataSampahActionPerformed

        String kategori = cmb_kategori_tmbhsampah.getSelectedItem().toString();
        String jenis = txt_jenissampah_tambah.getText();
        String deskripsi = txt_deskripsi_tambah.getText();
        String harga1Text = txt_hargadhs1_tambah.getText();
        String harga2Text = txt_hargadhs2_tambah.getText();
        String hargaPengepulText = txt_hargapgpl_tambah.getText();

        if (kategori.isEmpty() || jenis.isEmpty() || deskripsi.isEmpty()
                || harga1Text.isEmpty() || harga2Text.isEmpty() || hargaPengepulText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!");
            return;
        }

        try {
            double harga1 = Double.parseDouble(harga1Text);
            double harga2 = Double.parseDouble(harga2Text);
            double hargaPengepul = Double.parseDouble(hargaPengepulText);

            String kode = SampahUtil.generateKodeSampah(jenis); 
            txt_kodesampah_tambah.setText(kode); 

            try (Connection conn = com.koneksi.koneksi.konek()) {
                String sql = "INSERT INTO sampah (kode_sampah, kategori, jenis, deskripsi, harga1, harga2, harga_pengepul, stok, created_at, updated_at) "
                        + "VALUES (?, ?, ?, ?, ?, ?, ?, 0, NOW(), NOW())";

                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, kode);
                pst.setString(2, kategori);
                pst.setString(3, jenis);
                pst.setString(4, deskripsi);
                pst.setDouble(5, harga1);
                pst.setDouble(6, harga2);
                pst.setDouble(7, hargaPengepul);

                int rows = pst.executeUpdate();
                if (rows > 0) {
                    JOptionPane.showMessageDialog(this, "Data sampah berhasil disimpan!");
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menyimpan data.");
                }

                pst.close();
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Harga harus berupa angka.");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Kesalahan database: " + e.getMessage());
        }

    }//GEN-LAST:event_btn_simpahDataSampahActionPerformed

