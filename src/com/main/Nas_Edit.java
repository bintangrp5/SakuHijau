    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        String id = txt_id.getText().trim();
        String nama = txt_nama.getText().trim();
        String alamat = txt_alamat.getText().trim();
        String hp = txt_nohp.getText().trim();

// Validasi input
        if (nama.isEmpty() || alamat.isEmpty() || hp.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try (Connection conn = com.koneksi.koneksi.konek()) {
            String sql = "UPDATE nasabah SET nama = ?, alamat = ?, no_hp = ? WHERE id = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, nama);
            pst.setString(2, alamat);
            pst.setString(3, hp);
            pst.setString(4, id); 

            int result = pst.executeUpdate();

            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Data nasabah berhasil diupdate!");
                this.dispose(); 
            } else {
                JOptionPane.showMessageDialog(this, "Tidak ada data yang diperbarui. Periksa ID.", "Peringatan", JOptionPane.WARNING_MESSAGE);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal mengupdate data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btn_simpanActionPerformed

