    private void loadDataNasabah() {
        try (Connection conn = com.koneksi.koneksi.konek()) {
            String sql = "SELECT nama, alamat, no_hp FROM nasabah WHERE id = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, idNasabah);
            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                txt_id.setText(idNasabah);
                txt_nama.setText(rs.getString("nama"));
                txt_alamat.setText(rs.getString("alamat"));
                txt_nohp.setText(rs.getString("no_hp"));

                txt_id.setEditable(false); 
            }

            rs.close();
            pst.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data nasabah: " + e.getMessage());
        }
    }

